package net.sf.cglib.proxy;





public class A {

	private Long id;

	private String name;



	/**

	 * Returns the id.

	 * @return Long

	 */

	public Long getId() {

		return id;

	}



	/**

	 * Returns the name.

	 * @return String

	 */

	public String getName() {

		return name;

	}



	/**

	 * Sets the id.

	 * @param id The id to set

	 */

	public void setId(Long id) {

		this.id = id;

	}



	/**

	 * Sets the name.

	 * @param name The name to set

	 */

	public void setName(String name) {

		this.name = name;

	}



}

